import { React, useState } from 'react'
import './login.css'
import {loginSchema} from '../../Validations'

const Login = () => {

	const [nombre, setNombre] = useState('')
	const [password, setPassword] = useState('')
	const [errors, setErrors] = useState([]);

	const loginUser = async (e) => {
		e.preventDefault();

		const res = await fetch(`${process.env.REACT_APP_URL}/api/usuario/login`, {
			method: 'POST',
			headers: {
				'Content-Type': 'application/json',
			},
			credentials:'include',
			body: JSON.stringify({
				nombre,
				password,
			}),
		})
		
		if(res.ok){
			window.location.href='/'
		}else{
			setErrors(['Usuario o contraseña incorrectos'])
		}
	}

	const handleSubmit = async (e) => {
		e.preventDefault();
		try{
			let isValid = await loginSchema.validate({nombre, password}, {abortEarly: false});
			if(isValid){
				loginUser(e);
				setErrors([]);
			}
		}catch(e){
			setErrors(e.errors)
		}
	}

	return (
		<div className='container'>
			<div className="row justify-content-center">
				<div className="col-lg-4 col-md-6 col-12">
					<form onSubmit={handleSubmit}>
						<h1 className='text-center'>ControlMaq</h1>
						<br/>

						<label className='form-label'>Nombre</label>
						<input className='form-control' type="text" value={nombre}
							onChange={(e) => setNombre(e.target.value)}/>
						
						<br/>

						<label className='form-label'>Contraseña</label>
						<input className='form-control' type="password" value={password}
							onChange={(e) => setPassword(e.target.value)}/>
						
						<br/>
						<button className="btn btn-orange" type="submit" value="Login">Ingresar</button>
						<br /><br />

						{errors.length>0 &&
							<div className="alert alert-danger">
								{ errors.map((e) => (
										<div>
											{e}
										</div>
									))}
							</div>
						}
					</form>
				</div>
			</div>
		</div>
	)
}

export default Login